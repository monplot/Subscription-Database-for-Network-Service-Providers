CREATE MASTER KEY ENCRYPTION BY  
PASSWORD = 'password';

CREATE CERTIFICATE SelfSignedCertificate  
WITH SUBJECT = 'Password Encryption';  

CREATE SYMMETRIC KEY SQLSymmetricKey  
WITH ALGORITHM = AES_128  
ENCRYPTION BY CERTIFICATE SelfSignedCertificate;  

OPEN SYMMETRIC KEY SQLSymmetricKey  
DECRYPTION BY CERTIFICATE SelfSignedCertificate;  

--Encrypt
UPDATE GGSN  
SET [EncryptedPDP_CONTEXT] = EncryptByKey(Key_GUID('SQLSymmetricKey'), CONVERT(varchar, PDP_CONTEXT)); 
UPDATE SGSN  
SET [EncryptedPDP_CONTEXT] = EncryptByKey(Key_GUID('SQLSymmetricKey'), CONVERT(varchar, PDP_CONTEXT)); 

--Decrypt

SELECT SGSN_IP, PDP_CONTEXT,  
CONVERT(varchar, DecryptByKey(EncryptedPDP_CONTEXT)) AS 'DecryptedPDPCONTEXT'  
FROM SGSN; 
SELECT SGSN_IP, PDP_CONTEXT,  
CONVERT(varchar, DecryptByKey(EncryptedPDP_CONTEXT)) AS 'DecryptedPDPCONTEXT'  
FROM GGSN; 

--Close Symmetric key
CLOSE SYMMETRIC KEY SQLSymmetricKey;

